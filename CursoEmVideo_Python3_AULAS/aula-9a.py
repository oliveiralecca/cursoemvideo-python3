frase = 'Curso em Vídeo Python'
print(frase[3])   # mostra a letra de índice 3 (4ª)
print(frase[3:13])   # da 3 até a 12
print(frase[:13])   # do início até a 12
print(frase[13:])   # da letra 13 até o fim
print(frase[1:15])   # letra 1 até a 14
print(frase[1:15:2])   # da 1 até a 14, pulando 2
print(frase[1::2])   # da 1 até o fim, pulando 2
print(frase[::2])   # não sei início nem fim, str inteira pulando 2
