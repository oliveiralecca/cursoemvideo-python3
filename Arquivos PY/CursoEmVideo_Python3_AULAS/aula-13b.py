# CONTAGENS DE NÚMEROS:
for c in range(0, 6):   # de 0 a 5
    print(c)
print('FIM!')

print()

for c in range(1, 7):   # de 1 a 6
    print(c)
print('FIM!')

print()

for c in range(6, 0, -1):   # contagem regressiva 6 a 1 (-1 mostra o sentido da iteração: tirando 1 a cada repetição)
    print(c)
print('FIM!')

print()

for c in range(0, 7, 2):   # pulando de 2 em 2
    print(c)
print('FIM!')
